#O que aprendi sobre erros no python?
#  Aprendi que é normal e natural cometer erros, entendi os erros mais comuns e porquê eles acontecem, como por exemplo:
# SyntaxError acontece quando escrevemos algo que nãofaz parte da linguagem de Python, então ele não entende
# NameError acontece quando usamos uma variável que não existe, que não foi criada
# TypeError acontece quando tentamos misturar dois dados que são incompatíveis
# ValueError acontece quando damos um valor que não pode se converter (Ex: tranformar texto em Int)
# Mas acima de tudo, entendi que os erros não são nossos inimigos, e sim um guia que nos ajuda a resolver e melhoras o código.