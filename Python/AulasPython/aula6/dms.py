#Este programa mostra o dobro do primeiro número, metade do segundo e a soma dos dois números

n1 = float(input("Digite o primeiro número: "))
n2 = float(input("Digite o segundo número: "))
dobro = n1*2
metade = n2/2
soma = n1 + n2
print(f"O dobro do 1° é {dobro}")
print(f"A metade do 2° é {metade}")
print(f"A soma dos dois é {soma}")
